def test_rss_feeds():
    """Test RSS feed connectivity"""
    print("\nğŸ“¡ TESTING RSS FEEDS")
    print("=" * 30)
    
    try:
        import feedparser
        import requests
    except ImportError as e:
        print(f"âŒ Cannot test feeds - missing dependencies: {e}")
        return False
    
    feeds = [
        "https://blockclubchicago.org/feed/",
        "https://feeds.feedburner.com/chicagoist/chicagoist",
        "https://www.chicagotribune.com/arcio/rss/category/news/",
        "https://chicago.suntimes.com/feeds/all.rss.xml"
    ]
    
    successful_feeds = 0
    
    for feed_url in feeds:
        try:
            print(f"Testing: {feed_url}")
            
            # Quick connectivity test
            response = requests.head(feed_url, timeout=10)
            if response.status_code == 200:
                print(f"  âœ… HTTP OK ({response.status_code})")
            else:
                print(f"  âš ï¸  HTTP {response.status_code}")
            
            # Parse test
            feed = feedparser.parse(feed_url)
            if feed.entries:
                print(f"  âœ… {len(feed.entries)} articles found")
                successful_feeds += 1
            else:
                print(f"  âŒ No articles found")
                
        except Exception as e:
            print(f"  âŒ Error: {e}")
    
    print(f"\nFeed Summary: {successful_feeds}/{len(feeds)} feeds working")
    return successful_feeds > 0

